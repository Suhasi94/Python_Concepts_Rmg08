# integer

num = 10
print(float(num))       # 10.0
print(complex(num))     # 10+0j
print(bool(num))        # True

# float
num = 1.23
print(int(num))         # 1
print(complex(num))     # 1.23+0j
print(bool(num))        # True

# complex
num = 1+2j
# print(int(num))       # TypeError
# print(float(num))       # TypeError
print(bool(num))        # True

# boolean
a = True
b = False

print(int(a))           # 1
print(int(b))           # 0

print(float(a))         # 1.0
print(float(b))         # 0.0

print(complex(a))       # 1+0j
print(complex(b))       # 0j













